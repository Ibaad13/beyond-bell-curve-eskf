"""
Part 3 -- Filtering Results: Baseline vs. GPS-Error-Aware Architectures
==========================================================================
Reproduces Table IV / Table V and the filtering figures of the paper
directly from saved per-epoch filter output arrays. Output figure
filenames match paper.tex \\includegraphics calls exactly:

  fig9_biasou_horiz_error.png   AGZ: baseline vs OU, horizontal error vs time
  fig11_biasou_cdf.png          AGZ: baseline vs OU, empirical CDF
  fig10_estimated_bias.png      AGZ: estimated OU bias states
  d2_fig9_filter_comparison.png outdoor_1: baseline vs IMM / vs naive gating
  d2_fig10_ou_comparison.png    outdoor_1: baseline vs OU (time + CDF)
  d2_fig11_bias_states.png      outdoor_1: estimated OU bias states
  d2_fig12_rmse_bar_comparison.png  outdoor_1: RMSE/p95 summary, all 4 arch.

IMPORTANT -- fig8_filter_comparison.png (AGZ baseline vs. naive-gating/IMM)
is NOT regenerated by this script. The per-epoch arrays for the AGZ naive-
gating and AGZ IMM runs were not part of the saved package this repo was
built from (only baseline_results.npz and bias_results.npz exist for
AGZ) -- see README "Reproducibility notes". The original pre-rendered
fig8_filter_comparison.png (from the paper's own figures/ folder) is
shipped as a static file in this folder so paper.tex still compiles; if
you re-run naive_gating_ekf.py / imm_ekf.py for AGZ and save their output
as naive_gating_results.npz / imm_results.npz in data/dataset1/, extend
this script to regenerate it from real data.

Run:    python filtering_results.py
"""
import numpy as np
import pandas as pd
import matplotlib
matplotlib.use("Agg")
import matplotlib.pyplot as plt
from pathlib import Path

plt.rcParams.update({
    "figure.dpi": 150, "savefig.dpi": 150, "font.size": 10,
    "axes.grid": True, "grid.alpha": 0.3, "axes.axisbelow": True,
})
COL_BASELINE, COL_OU, COL_IMM, COL_GATING = (
    "tab:blue", "tab:purple", "tab:green", "tab:red")

OUT_FIG = Path("figures"); OUT_FIG.mkdir(exist_ok=True)
OUT_TAB = Path("tables");  OUT_TAB.mkdir(exist_ok=True)

def horiz(err):
    return np.hypot(err[:, 0], err[:, 1])

def metrics(err):
    h = horiz(err)
    rmse = lambda a: np.sqrt(np.mean(a ** 2))
    return dict(
        rmse_x=rmse(err[:, 0]), rmse_y=rmse(err[:, 1]), rmse_z=rmse(err[:, 2]),
        rmse_h=rmse(h), rmse_3d=rmse(np.hypot(h, err[:, 2])),
        median_h=np.median(h), p95_h=np.percentile(h, 95), max_h=h.max(),
    )

# =======================================================================
# DATASET 1 (AGZ) -- Table IV, Figs 9/10/11
# =======================================================================
d1_base = np.load("data/dataset1/baseline_results.npz")
d1_ou   = np.load("data/dataset1/bias_results.npz")

m_base = metrics(d1_base["err"])
m_ou   = metrics(d1_ou["err"])

# Paper-reported reference numbers for naive gating / IMM on AGZ (Table IV
# of the paper) -- included for completeness since raw arrays for those two
# runs were not part of the saved package; NOT recomputed from data.
paper_gating = dict(rmse_x=5.94, rmse_y=7.46, rmse_z=2.64, rmse_h=9.54,
                     rmse_3d=9.90, median_h=4.28, p95_h=21.40, max_h=37.31)
paper_imm    = dict(rmse_x=3.46, rmse_y=5.04, rmse_z=2.64, rmse_h=6.12,
                     rmse_3d=6.66, median_h=3.79, p95_h=12.05, max_h=27.54)

table4 = pd.DataFrame({
    "Metric": list(m_base.keys()),
    "Baseline_Gaussian_R (from data)": [round(m_base[k], 3) for k in m_base],
    "Naive_Gating (paper-reported)":   [paper_gating[k] for k in m_base],
    "2-Mode_IMM (paper-reported)":     [paper_imm[k] for k in m_base],
    "Bias-Augmented_OU (from data)":   [round(m_ou[k], 3) for k in m_base],
})
table4.to_csv(OUT_TAB / "table4_agz_filtering_results.csv", index=False)
print("Table IV (AGZ):\n", table4.to_string(index=False))

pct_h = 100 * (m_ou["rmse_h"] - m_base["rmse_h"]) / m_base["rmse_h"]
pct_p95 = 100 * (m_ou["p95_h"] - m_base["p95_h"]) / m_base["p95_h"]
print(f"\nOU vs baseline: horizontal RMSE {pct_h:+.1f}%, p95 {pct_p95:+.1f}%")

t0_base = d1_base["t"] - d1_base["t"][0]
t0_ou = d1_ou["t"] - d1_ou["t"][0]
h_base = horiz(d1_base["err"])
h_ou = horiz(d1_ou["err"])

# ---- fig9_biasou_horiz_error.png ----
plt.figure(figsize=(7, 5))
plt.plot(t0_base, h_base, color=COL_BASELINE, lw=0.9,
         label=f"Baseline (Gaussian $R$), RMSE={m_base['rmse_h']:.2f} m")
plt.plot(t0_ou, h_ou, color=COL_OU, lw=0.9,
         label=f"Bias-augmented OU (18-state), RMSE={m_ou['rmse_h']:.2f} m")
plt.xlabel("Time (s)"); plt.ylabel("Horizontal error (m)")
plt.title("Baseline vs. Bias-Augmented OU ESKF -- horizontal error")
plt.legend(fontsize=8); plt.tight_layout()
plt.savefig(OUT_FIG / "fig9_biasou_horiz_error.png"); plt.close()

# ---- fig11_biasou_cdf.png ----
plt.figure(figsize=(7, 5))
for name, h, col in [("Baseline", h_base, COL_BASELINE), ("Bias-augmented OU", h_ou, COL_OU)]:
    hs = np.sort(h); cdf = np.arange(1, len(hs) + 1) / len(hs)
    plt.plot(hs, cdf, color=col, lw=1.6, label=name)
plt.xlabel("Horizontal error (m)"); plt.ylabel("CDF")
plt.title("Empirical CDF of horizontal position error")
plt.legend(fontsize=8, loc="lower right"); plt.tight_layout()
plt.savefig(OUT_FIG / "fig11_biasou_cdf.png"); plt.close()

# ---- fig10_estimated_bias.png ----
bias = d1_ou["bias"]
labels3 = ["East (x) bias $b_{gps,x}$ (m)", "North (y) bias $b_{gps,y}$ (m)", "Up (z) bias $b_{gps,z}$ (m)"]
fig, axes = plt.subplots(3, 1, figsize=(7, 7), sharex=True)
for i in range(3):
    axes[i].plot(t0_ou, bias[:, i], color=COL_OU, lw=1)
    axes[i].set_ylabel(labels3[i], fontsize=8)
axes[-1].set_xlabel("Time (s)")
fig.suptitle("Estimated Ornstein-Uhlenbeck GPS bias states $b_{gps}(t)$")
plt.tight_layout(); plt.savefig(OUT_FIG / "fig10_estimated_bias.png"); plt.close()

print(f"\nANEES: baseline={d1_base['nees'].mean():.1f}, bias-OU={d1_ou['nees'].mean():.1f}  (ideal=3)")

# =======================================================================
# DATASET 2 (outdoor_1) -- Table V, Figs d2_fig9-12  (fully data-derived)
# =======================================================================
files = dict(Baseline="data/dataset2/d2_baseline_results.npz",
             Naive_Gating="data/dataset2/d2_gating_results.npz",
             IMM_2Mode="data/dataset2/d2_imm_results.npz",
             Bias_Aug_OU="data/dataset2/d2_ou_results.npz")
d2 = {name: np.load(path) for name, path in files.items()}
d2_metrics = {name: metrics(d["err"]) for name, d in d2.items()}

table5 = pd.DataFrame(d2_metrics).T.round(3)
table5["delta_rmse_h_pct"] = 100 * (table5["rmse_h"] - table5.loc["Baseline", "rmse_h"]) / table5.loc["Baseline", "rmse_h"]
table5["delta_p95_h_pct"] = 100 * (table5["p95_h"] - table5.loc["Baseline", "p95_h"]) / table5.loc["Baseline", "p95_h"]
table5.to_csv(OUT_TAB / "table5_outdoor1_filtering_results.csv")
print("\nTable V (outdoor_1):\n", table5.round(3).to_string())

t0 = d2["Baseline"]["t"] - d2["Baseline"]["t"][0]

# ---- d2_fig9_filter_comparison.png ----
fig, axes = plt.subplots(2, 1, figsize=(7, 8))
axes[0].plot(t0, horiz(d2["Baseline"]["err"]), color=COL_BASELINE, label="Baseline (Gaussian $R$)")
axes[0].plot(t0, horiz(d2["IMM_2Mode"]["err"]), color=COL_IMM, label="2-Mode IMM")
axes[0].set_title("Dataset 2: Baseline vs. 2-Mode Gaussian-Sum/IMM")
axes[0].set_ylabel("Horizontal error (m)"); axes[0].legend(fontsize=8)
axes[1].plot(t0, horiz(d2["Baseline"]["err"]), color=COL_BASELINE, label="Baseline (Gaussian $R$)")
axes[1].plot(t0, horiz(d2["Naive_Gating"]["err"]), color=COL_GATING, label="Naive outlier gating/inflation")
axes[1].set_title("Dataset 2: Baseline vs. Naive Outlier Gating")
axes[1].set_xlabel("Time (s)"); axes[1].set_ylabel("Horizontal error (m)"); axes[1].legend(fontsize=8)
plt.tight_layout(); plt.savefig(OUT_FIG / "d2_fig9_filter_comparison.png"); plt.close()

# ---- d2_fig10_ou_comparison.png ----
h_base2 = horiz(d2["Baseline"]["err"]); h_ou2 = horiz(d2["Bias_Aug_OU"]["err"])
fig, axes = plt.subplots(2, 1, figsize=(7, 8))
axes[0].plot(t0, h_base2, color=COL_BASELINE, lw=1.1,
             label=f"Baseline (Gaussian $R$), RMSE={d2_metrics['Baseline']['rmse_h']:.2f} m")
axes[0].plot(t0, h_ou2, color=COL_OU, lw=1.1,
             label=f"Bias-augmented OU (18-state), RMSE={d2_metrics['Bias_Aug_OU']['rmse_h']:.2f} m")
axes[0].set_title("Dataset 2: Baseline vs. Bias-Augmented OU ESKF -- horizontal error")
axes[0].set_xlabel("Time (s)"); axes[0].set_ylabel("Horizontal error (m)"); axes[0].legend(fontsize=8)
for name, h, col in [("Baseline", h_base2, COL_BASELINE), ("Bias-augmented OU", h_ou2, COL_OU)]:
    hs = np.sort(h); cdf = np.arange(1, len(hs) + 1) / len(hs)
    axes[1].plot(hs, cdf, color=col, lw=1.6, label=name)
axes[1].set_title("Empirical CDF of horizontal position error")
axes[1].set_xlabel("Horizontal error (m)"); axes[1].set_ylabel("CDF"); axes[1].legend(fontsize=8, loc="lower right")
plt.tight_layout(); plt.savefig(OUT_FIG / "d2_fig10_ou_comparison.png"); plt.close()

# ---- d2_fig11_bias_states.png ----
bias2 = d2["Bias_Aug_OU"]["bias"]
fig, axes = plt.subplots(3, 1, figsize=(7, 7), sharex=True)
for i in range(3):
    axes[i].plot(t0, bias2[:, i], color=COL_OU, lw=1)
    axes[i].set_ylabel(labels3[i], fontsize=8)
axes[-1].set_xlabel("Time (s)")
fig.suptitle("Dataset 2: Estimated Ornstein-Uhlenbeck GPS bias states")
plt.tight_layout(); plt.savefig(OUT_FIG / "d2_fig11_bias_states.png"); plt.close()

# ---- d2_fig12_rmse_bar_comparison.png ----
fig, axes = plt.subplots(1, 2, figsize=(10, 5))
names = list(d2_metrics.keys())
bar_colors = [COL_BASELINE, COL_GATING, "tab:orange", "tab:green"]
axes[0].bar(names, [d2_metrics[n]["rmse_h"] for n in names], color=bar_colors)
axes[0].set_ylabel("Horizontal RMSE (m)"); axes[0].set_title("Dataset 2: Horizontal RMSE by architecture")
axes[0].tick_params(axis="x", rotation=20)
axes[1].bar(names, [d2_metrics[n]["p95_h"] for n in names], color=bar_colors)
axes[1].set_ylabel("$p_{95}$ horizontal error (m)"); axes[1].set_title("Dataset 2: $p_{95}$ horizontal error by architecture")
axes[1].tick_params(axis="x", rotation=20)
plt.tight_layout(); plt.savefig(OUT_FIG / "d2_fig12_rmse_bar_comparison.png"); plt.close()

print("\nDone. Figures -> figures/, Tables -> tables/")
print("\nNOTE: fig8_filter_comparison.png (AGZ gating/IMM) is shipped as a "
      "static pre-rendered file in this folder -- see the module docstring "
      "above for why it isn't regenerated by this script.")
